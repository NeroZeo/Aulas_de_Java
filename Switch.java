
public class Switch {

	public static void main(String[] args) {
		
		int turno = 1;
		switch (turno) {
		case 1:
			System.out.println("Manha");
			break;
		case 2:
			System.out.println("Tarde");
			break;
		case 3:
			System.out.println("Noite");
			break;
		default:
			System.out.println("Opção Invalida");
		}

	}

}
